@section('isihalaman')
    <h3>Apa Itu Layanan Pengaduan Masyarakat?</h3>
    <p>
    <h4>Pengenalan</h4>
        Layanan Pengaduan Masyarakat adalah platform yang dirancang untuk memfasilitasi masyarakat dalam menyampaikan 
        keluhan, kritik, atau saran mengenai layanan publik. Layanan ini bertujuan untuk meningkatkan transparansi dan 
        kualitas pelayanan yang diberikan oleh institusi terkait.

    <p>
    <h4>Tujuan Layanan Pengaduan</h4>
        Layanan ini bertujuan untuk:
        <ul>
            <li>Meningkatkan transparansi dalam penyampaian dan penanganan keluhan masyarakat.</li>
            <li>Memastikan keluhan masyarakat ditangani dengan cepat, tepat, dan efisien.</li>
            <li>Memberikan umpan balik yang konstruktif kepada penyedia layanan untuk meningkatkan kualitas layanan.</li>
        </ul>

    <p>
    <h4>Bagaimana Layanan Pengaduan Bekerja?</h4>
        Proses layanan pengaduan melibatkan beberapa tahap, yaitu:
        <ol>
            <li><b>Pengajuan Pengaduan:</b> Masyarakat dapat menyampaikan pengaduan melalui platform ini dengan mengisi 
            formulir pengaduan yang tersedia.</li>
            <li><b>Penanganan dan Verifikasi:</b> Pengaduan yang diterima akan diverifikasi dan diteruskan ke pihak terkait 
            untuk ditindaklanjuti.</li>
            <li><b>Umpan Balik:</b> Hasil dari penanganan pengaduan akan disampaikan kembali kepada pelapor.</li>
        </ol>

    <p>
    <h4>Dasar Hukum</h4>
    Layanan Pengaduan Masyarakat didasarkan pada peraturan perundang-undangan yang berlaku, seperti UU No. 25 Tahun 2009 
    tentang Pelayanan Publik, yang menjamin hak masyarakat untuk menyampaikan pengaduan terhadap layanan publik yang 
    dianggap tidak memadai.

    <p>
        Dengan adanya layanan pengaduan ini, diharapkan masyarakat dapat lebih aktif berpartisipasi dalam pengawasan 
        layanan publik, sehingga tercipta pelayanan yang lebih baik, responsif, dan sesuai dengan kebutuhan masyarakat.
@endsection
